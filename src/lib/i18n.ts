export type TranslationKey = keyof typeof translations.en;

export function useTranslation(lang: Language | undefined) {
  const safeLang: Language = lang && (lang === 'en' || lang === 'de') ? lang : 'en';
  return (key: TranslationKey | string) => {
    const translationKey = key as TranslationKey;
    return translations[safeLang][translationKey] || translations.en[translationKey] || key;
  };
}

